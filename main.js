var StatisticsService = class {
  constructor(app, plugin) {
    this.statsPath = ".obsidian/plugins/ai-workbench/stats.json";
    this.app = app;
    this.plugin = plugin;
    this.stats = this.getDefaultStats();
  }
  getDefaultStats() {
    return {
      totalRequests: 0,
      successfulRequests: 0,
      failedRequests: 0,
      totalTokensUsed: 0,
      promptTokens: 0,
      completionTokens: 0,
      actionsCount: {},
      lastReset: Date.now()
    };
  }
  /**
   * Load statistics from file
   */
  async load() {
    try {
      const adapter = this.app.vault.adapter;
      if (await adapter.exists(this.statsPath)) {
        const content = await adapter.read(this.statsPath);
        this.stats = JSON.parse(content);
      }
    } catch (error) {
      console.error("Failed to load stats:", error);
      this.stats = this.getDefaultStats();
    }
  }
  /**
   * Save statistics to file
   */
  async save() {
    try {
      const adapter = this.app.vault.adapter;
      await adapter.write(this.statsPath, JSON.stringify(this.stats, null, 2));
    } catch (error) {
      console.error("Failed to save stats:", error);
    }
  }
  /**
   * Record a successful request
   */
  async recordSuccess(action, tokens) {
    this.stats.totalRequests++;
    this.stats.successfulRequests++;
    if (tokens) {
      this.stats.totalTokensUsed += tokens.total;
      this.stats.promptTokens += tokens.prompt;
      this.stats.completionTokens += tokens.completion;
    }
    this.stats.actionsCount[action] = (this.stats.actionsCount[action] || 0) + 1;
    await this.save();
  }
  /**
   * Record a failed request
   */
  async recordFailure() {
    this.stats.totalRequests++;
    this.stats.failedRequests++;
    await this.save();
  }
  /**
   * Get current statistics
   */
  getStats() {
    return { ...this.stats };
  }
  /**
   * Reset statistics
   */
  async reset() {
    this.stats = this.getDefaultStats();
    await this.save();
  }
  /**
   * Get usage summary for display
   */
  getSummary() {
    const successRate = this.stats.totalRequests > 0 ? Math.round(this.stats.successfulRequests / this.stats.totalRequests * 100) + "%" : "0%";
    const totalTokens = this.formatTokens(this.stats.totalTokensUsed);
    const topActions = Object.entries(this.stats.actionsCount).sort((a, b) => b[1] - a[1]).slice(0, 5).map(([action, count]) => ({ action, count }));
    return {
      totalRequests: this.stats.totalRequests,
      successRate,
      totalTokens,
      topActions
    };
  }
  /**
   * Format token count
   */
  formatTokens(count) {
    if (count >= 1e6) {
      return (count / 1e6).toFixed(1) + "M";
    } else if (count >= 1e3) {
      return (count / 1e3).toFixed(1) + "K";
    }
    return count.toString();
  }
  /**
   * Get estimated cost (approximate)
   */
  getEstimatedCost(pricePerMillion = 0.15) {
    const cost = this.stats.totalTokensUsed / 1e6 * pricePerMillion;
    return "$" + cost.toFixed(4);
  }
};

