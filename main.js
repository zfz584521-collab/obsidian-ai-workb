var StatusBarService = class {
  constructor(app, plugin) {
    this.statusBarEl = null;
    this.isEnabled = false;
    this.isProcessing = false;
    this.currentAction = "";
    this.tokenCount = null;
    this.lastError = null;
    this.app = app;
    this.plugin = plugin;
  }
  /**
   * Initialize status bar
   */
  init(enabled) {
    this.isEnabled = enabled;
    if (!enabled)
      return;
    this.statusBarEl = this.plugin.addStatusBarItem();
    this.statusBarEl.addClass("ai-workbench-status");
    this.render();
  }
  /**
   * Update enabled state
   */
  setEnabled(enabled) {
    this.isEnabled = enabled;
    if (enabled && !this.statusBarEl) {
      this.init(true);
    } else if (!enabled && this.statusBarEl) {
      this.statusBarEl.remove();
      this.statusBarEl = null;
    }
  }
  /**
   * Set processing state
   */
  setProcessing(action) {
    this.isProcessing = true;
    this.currentAction = action;
    this.lastError = null;
    this.render();
  }
  setProgress(action, completed, total) {
    this.setProcessing(`${action} ${completed}/${total} \u5F20\u56FE\u7247`);
  }
  /**
   * Set completed state
   */
  setCompleted(tokens) {
    this.isProcessing = false;
    this.currentAction = "";
    this.tokenCount = typeof tokens === "number" ? { prompt: 0, completion: 0, total: tokens } : tokens || null;
    this.render();
    if (tokens) {
      setTimeout(() => {
        if (!this.isProcessing) {
          this.tokenCount = null;
          this.render();
        }
      }, 5e3);
    }
  }
  /**
   * Set error state
   */
  setError(error) {
    this.isProcessing = false;
    this.currentAction = "";
    this.lastError = error;
    this.render();
    setTimeout(() => {
      this.lastError = null;
      this.render();
    }, 1e4);
  }
  /**
   * Render status bar
   */
  render() {
    if (!this.statusBarEl || !this.isEnabled)
      return;
    this.statusBarEl.empty();
    if (this.isProcessing) {
      this.statusBarEl.addClass("processing");
      this.statusBarEl.createSpan({ text: "\u23F3 " });
      this.statusBarEl.createSpan({ text: this.currentAction + "..." });
      return;
    }
    if (this.lastError) {
      this.statusBarEl.addClass("error");
      this.statusBarEl.createSpan({ text: "\u274C \u9519\u8BEF" });
      this.statusBarEl.setAttribute("aria-label", this.lastError);
      return;
    }
    if (this.tokenCount) {
      this.statusBarEl.createSpan({ text: `\u2728 ${this.tokenCount.total} tokens` });
      return;
    }
    this.statusBarEl.removeClass("processing", "error");
    this.statusBarEl.createSpan({ text: "\u2728 AI" });
    this.statusBarEl.setAttribute("aria-label", "AI Workbench \u5C31\u7EEA");
  }
};

