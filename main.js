var import_obsidian5 = require("obsidian");
var UndoService = class {
  constructor(app, backupManager, getHistory) {
    this.app = app;
    this.backupManager = backupManager;
    this.getHistory = getHistory;
  }
  /**
   * Undo the last successful operation
   */
  async undoLast() {
    const history = this.getHistory();
    const lastOp = history.find((h) => h.success);
    if (!lastOp) {
      new import_obsidian5.Notice("\u6CA1\u6709\u53EF\u64A4\u9500\u7684\u64CD\u4F5C");
      return false;
    }
    const backups = await this.backupManager.listBackups();
    const noteName = lastOp.noteTitle;
    const recentBackup = backups.find(
      (b) => (b.originalPath === lastOp.notePath || !b.originalPath && b.originalFile === noteName) && b.createdAt.getTime() <= lastOp.timestamp
    );
    if (!recentBackup) {
      new import_obsidian5.Notice("\u672A\u627E\u5230\u5907\u4EFD\u6587\u4EF6");
      return false;
    }
    const file = this.app.vault.getAbstractFileByPath(lastOp.notePath);
    if (!(file instanceof import_obsidian5.TFile)) {
      new import_obsidian5.Notice("\u6587\u4EF6\u4E0D\u5B58\u5728");
      return false;
    }
    const success = await this.backupManager.restore(recentBackup.path, lastOp.notePath);
    if (success) {
      new import_obsidian5.Notice(`\u5DF2\u64A4\u9500: ${lastOp.actionName}`);
      return true;
    }
    return false;
  }
  /**
   * Undo a specific operation
   */
  async undoOperation(entry) {
    if (!entry.success) {
      new import_obsidian5.Notice("\u8BE5\u64CD\u4F5C\u5931\u8D25\uFF0C\u65E0\u9700\u64A4\u9500");
      return false;
    }
    const backups = await this.backupManager.listBackups();
    const noteName = entry.noteTitle;
    const backup = backups.find(
      (b) => (b.originalPath === entry.notePath || !b.originalPath && b.originalFile === noteName) && Math.abs(b.createdAt.getTime() - entry.timestamp) < 6e4
      // Within 1 minute
    );
    if (!backup) {
      new import_obsidian5.Notice("\u672A\u627E\u5230\u5BF9\u5E94\u7684\u5907\u4EFD\u6587\u4EF6");
      return false;
    }
    const file = this.app.vault.getAbstractFileByPath(entry.notePath);
    if (!(file instanceof import_obsidian5.TFile)) {
      new import_obsidian5.Notice("\u6587\u4EF6\u4E0D\u5B58\u5728");
      return false;
    }
    const success = await this.backupManager.restore(backup.path, entry.notePath);
    if (success) {
      new import_obsidian5.Notice(`\u5DF2\u64A4\u9500: ${entry.actionName}`);
      return true;
    }
    return false;
  }
  /**
   * Check if undo is available
   */
  canUndo() {
    const history = this.getHistory();
    return history.some((h) => h.success);
  }
};

